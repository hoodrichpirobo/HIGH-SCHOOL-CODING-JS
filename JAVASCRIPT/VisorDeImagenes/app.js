function pulsauno(){
    var imagenpequeña = document.getElementById("pequeñauno")
    var rutaimagen = imagenpequeña.src;
    var texto=imagenpequeña.alt
    var imagengrande = document.getElementById("imagenvisor")
    imagengrande.src=rutaimagen;
    var pie=document.getElementById("piefoto");
    pie.innerHTML=texto;
}
